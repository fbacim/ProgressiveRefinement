# updateView()
# updates camera for main application
def updateView():
	# make sure we have the wiimote and nunchuk connected
	if wiimote:
		if wiimote.nunchuk:
			# first we treat yaw, joystick data is normalized between -1 and 1
			if math.fabs(wiimote.nunchuk.position[0]) > 0.1:
				viz.MainView.setEuler([wiimote.nunchuk.position[0]*Navigation.rotation_speed*viz.elapsed()*2.0,0,0],viz.HEAD_ORI,viz.REL_LOCAL)
			# then we treat travel
			if math.fabs(wiimote.nunchuk.position[1]) > 0.1:
				viz.MainView.move(0,0,wiimote.nunchuk.position[1]*Navigation.translation_speed*viz.elapsed(),HEAD_ORI)
	
	if Navigation.yaw == Rotate.CW: #right
		viz.MainView.setEuler([Navigation.rotation_speed*viz.elapsed()*2.0,0,0],viz.HEAD_ORI,viz.REL_LOCAL)
	elif Navigation.yaw == Rotate.CCW: #left
		viz.MainView.setEuler([-Navigation.rotation_speed*viz.elapsed()*2.0,0,0],viz.HEAD_ORI,viz.REL_LOCAL)
		
	if Navigation.pitch == Rotate.CW: #down
		viz.MainView.setEuler([0,Navigation.rotation_speed*viz.elapsed(),0],viz.HEAD_ORI,viz.REL_LOCAL)
	elif Navigation.pitch == Rotate.CCW: #up
		viz.MainView.setEuler([0,-Navigation.rotation_speed*viz.elapsed(),0],viz.HEAD_ORI,viz.REL_LOCAL)
	
	if Navigation.roll == Rotate.CW: #down
		viz.MainView.setEuler([0,0,Navigation.rotation_speed*viz.elapsed()],viz.HEAD_ORI,viz.REL_LOCAL)
	elif Navigation.roll == Rotate.CCW: #up
		viz.MainView.setEuler([0,0,-Navigation.rotation_speed*viz.elapsed()],viz.HEAD_ORI,viz.REL_LOCAL)

	if Navigation.fly == Move.UP:
		viz.MainView.move(0,Navigation.TRANSLATION_SPEED/2*viz.elapsed(),0,viz.HEAD_ORI)
	elif Navigation.fly == Move.DOWN:
		viz.MainView.move(0,-Navigation.TRANSLATION_SPEED/2*viz.elapsed(),0,viz.HEAD_ORI)

