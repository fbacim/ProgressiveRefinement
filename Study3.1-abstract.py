	dataFileWriter.writerow([participantNumber,selectedTechnique,'supermarket_'+str(applicationMode),Globals.STUDY_MODE,currentTrial,taskTimer.stop(),0,hit,selectionTechnique[Globals.technique].userdata,screenpos[0],screenpos[1]])
