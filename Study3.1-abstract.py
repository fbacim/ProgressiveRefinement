# createQuad()
# creates a rectangle on the top portion of the screen to display the current technique
def createQuad(left_bot,left_top,right_bot,right_top):
	viz.startlayer(viz.QUADS) 
	viz.vertexcolor(0,0,0)
	viz.vertex(left_bot)
	viz.vertex(left_top)
	viz.vertex(right_top)
	viz.vertex(right_bot)
	return viz.endlayer()

def createCircle(radius,x=0.0,y=0.0,z=0.0):
	viz.startlayer(viz.TRIANGLE_FAN) 
	viz.vertexcolor(0,0,0)
	viz.vertex(x,y,z)
	detail = 15
	for i in range(int(360.0/detail)+1):
		viz.vertex(x+radius*math.sin(float(i)*detail*math.pi/180.0),y+radius*math.cos(float(i)*detail*math.pi/180.0),z)
		#print "[",(x+radius*math.sin(float(i)*detail*math.pi/180.0)),",",(y+radius*math.cos(float(i)*detail*math.pi/180.0)),"]"
	return viz.endlayer()

# resetObjects()
# resets objects to their original positions
def resetObjects():
	global children
	global supermarket
	
	# create identity matrix to be used in transformations
	identity = vizmat.Transform()
	identity.makeIdent()
	
	index = 0
	
	for child in children:
		obj_size = object_size[index]#child.getBoundingBox(viz.ABS_GLOBAL).size
		#print child,": ",obj_size
		if obj_size[0] < 40 and obj_size[1] < 40 and obj_size[2] < 40:
			parents = child.getParents()
			for parent in parents:
				if parent != supermarket:
					child.unparent(parent)
					child.parent(supermarket)
					break
			child.setMatrix(identity,viz.ABS_GLOBAL)
			object_position[index] = child.getBoundingSphere(viz.ABS_GLOBAL).center
		index += 1

