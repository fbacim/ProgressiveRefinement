def selectTarget():
	global trials
	global currentTrial

	#print selectionTechnique[Globals.technique].selectedObject.getNodeNames(),trials[currentTrial][2].getNodeNames()
	
	# check if it's a hit or miss, and if it's the final selection (if not, it won't be either)
